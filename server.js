// Middleware to parse JSON request bodies
app.use(express.json());

// Endpoint for staking tokens
app.post('/stake', async (req, res) => {
  const { account, amount } = req.body;
  try {
    await contract.methods.stakeTokens(amount).send({ from: account });
    res.send("Tokens staked successfully.");
  } catch (error) {
    res.status(500).send(error.message);
  }
});

// Endpoint to become juror
app.post('/juror', async (req, res) => {
  const { account } = req.body;
  try {
    await contract.methods.becomeJuror().send({ from: account });
    res.send("Juror status granted.");
  } catch (error) {
    res.status(500).send(error.message);
  }
});

// Endpoint to vote on a challenge
app.post('/vote', async (req, res) => {
  const { account, challengeId, vote } = req.body;
  try {
    await contract.methods.voteOnChallenge(challengeId, vote).send({ from: account });
    res.send("Vote cast successfully.");
  } catch (error) {
    res.status(500).send(error.message);
  }
});

app.listen(port, () => {
  console.log(`Server running on http://localhost:${port}`);
});
